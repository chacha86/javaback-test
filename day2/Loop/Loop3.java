package day2.Loop;

public class Loop3 {
    public static void main(String[] args) {

        // 2중 반복문, 중첩 반복문

        // 1,2,3,4,5 를 10번 출력
        for(int i = 0; i < 10; i++) {
            for(int j = 1; j <= 5; j = j + 1) {
                System.out.println(j);
            }
        }





    }
}
